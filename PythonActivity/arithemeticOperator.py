# Python program to demonstrate the 
# example of arithmetic operators

a = 10
b = 3

# addition
result = a+b
print("a+b :", result)

# subtraction
result = a-b
print("a-b :", result)

# division
result = a/b
print("a/b :", result)

# modulus
result = a%b
print("a%b :", result)

# exponent
result = a**b
print("a**b :", result)

# floor division
result = a//b
print("a//b :", result)

# updating the values of a & b 
a = -10
b = 3
print("a:", a, "b:", b)
result = a//b
print("a//b :", result)